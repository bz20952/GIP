<section>
	In order to comply with GDPR, we do not collect any personal data.
    <br><br>

    The only data we collect are performance metrics for each subtask in Task 5 (the final task). These metrics are:
    <ul>
        <li>Time taken to complete subtask</li>
        <li>Number of attempts taken to complete subtask</li>
    </ul>
    <br>

